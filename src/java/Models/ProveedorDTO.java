public class ProveedorDTO {
    private String  razonProveedor, rucProveedor, telefonoProveedor, direccionProveedor, urlProveedor;

    public String getRazonProveedor() {
        return razonProveedor;
    }

    public void setRazonProveedor(String razonProveedor) {
        this.razonProveedor = razonProveedor;
    }

    public String getRucProveedor() {
        return rucProveedor;
    }

    public void setRucProveedor(String rucProveedor) {
        this.rucProveedor = rucProveedor;
    }

    public String getTelefonoProveedor() {
        return telefonoProveedor;
    }

    public void setTelefonoProveedor(String telefonoProveedor) {
        this.telefonoProveedor = telefonoProveedor;
    }

    public String getDireccionProveedor() {
        return direccionProveedor;
    }

    public void setDireccionProveedor(String direccionProveedor) {
        this.direccionProveedor = direccionProveedor;
    }

    public String getUrlProveedor() {
        return urlProveedor;
    }

    public void setUrlProveedor(String urlProveedor) {
        this.urlProveedor = urlProveedor;
    }
    
    
}
