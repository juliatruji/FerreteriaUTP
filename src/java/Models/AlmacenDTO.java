package Models;

/**
 *
 * @author Usuario
 */
public class AlmacenDTO {
    private String nombreAlmacen, ubicacionAlmacen, telefonoAlmacen, urlAlmacen;

    public String getNombreAlmacen() {
        return nombreAlmacen;
    }

    public void setNombreAlmacen(String nombreAlmacen) {
        this.nombreAlmacen = nombreAlmacen;
    }

    public String getUbicacionAlmacen() {
        return ubicacionAlmacen;
    }

    public void setUbicacionAlmacen(String ubicacionAlmacen) {
        this.ubicacionAlmacen = ubicacionAlmacen;
    }

    public String getTelefonoAlmacen() {
        return telefonoAlmacen;
    }

    public void setTelefonoAlmacen(String telefonoAlmacen) {
        this.telefonoAlmacen = telefonoAlmacen;
    }

    public String getUrlAlmacen() {
        return urlAlmacen;
    }

    public void setUrlAlmacen(String urlAlmacen) {
        this.urlAlmacen = urlAlmacen;
    }
    
    
}

